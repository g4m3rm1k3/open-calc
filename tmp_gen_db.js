function getSlug(title) {
  return title.toLowerCase()
    .replace(/[^\w\s-]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-');
}

const dbEntries = videos.map(v => {
  const url = v.embedCode.match(/src="([^"]+)"/)[1];
  const id = `vb-${getSlug(v.title)}`;
  return `    '${id}': { title: '${v.title.replace(/'/g, "\\'")}', url: '${url}', source: 'Michel van Biezen' },`;
}).join('\n');

